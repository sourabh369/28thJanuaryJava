package loops;

public class Sample4 
{

	public static void main(String[] args) 
	{
	
		for(int i=0;i<=10;i++)
		{
			System.out.println(i);
		}
		
		for(int i=5;i<=50;i = i + 5)
		{
			System.out.println(i);
		}
		
		int i = 5;
		
		while(i<=50)
		{
			System.out.println(i);
			i = i +5;
		}
		
	}
	
}
